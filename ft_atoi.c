#include "libft.h"

int	ft_atoi(const char *c)
{
	unsigned int	i;
	int				r;
	int				s;

	i = 0;
	r = 0;
	if (c[0] == '-')
	{
		s = -1;
		i++;
	}
	else
		s = 1;
	while (c[i])
	{
		r = r * 10 + (c[i] - '0');
		i++;
	}
	return (r * s);
}
